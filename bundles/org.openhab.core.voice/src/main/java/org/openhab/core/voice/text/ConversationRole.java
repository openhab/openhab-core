package org.openhab.core.voice.text;

import org.eclipse.jdt.annotation.NonNullByDefault;

/**
 * The {@link org.openhab.core.voice.text.ConversationRole} class contains a list of messages in between the users and a
 * LanguageInterpreter
 *
 * @author Miguel Álvarez Díez - Initial contribution
 */
@NonNullByDefault
public enum ConversationRole {
    /** For user input messages **/
    USER,
    /** For messages generated by an interpreter **/
    OPENHAB,
    /** For LLM thinking **/
    THINKING,
    /** For LLM tool calling **/
    TOOL_CALL,
    /** For LLM tool call calling result **/
    TOOL_RETURN;
}
