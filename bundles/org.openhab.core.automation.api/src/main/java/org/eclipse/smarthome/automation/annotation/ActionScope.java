import java.lang.annotation.Target;

/**
 * Scope definition for an action module
 *
 * @author Stefan Triller - initial contribution
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface ActionScope {

    String name();

}
